inline void DES_IP(u32 *X, u32 *Y, u32 *T)
{
	*T = ((*X >>  4) ^ *Y) & 0x0f0f0f0f; *Y ^= *T; *X ^= (*T <<  4);
  	*T = ((*X >> 16) ^ *Y) & 0x0000ffff; *Y ^= *T; *X ^= (*T << 16);
    	*T = ((*Y >>  2) ^ *X) & 0x33333333; *X ^= *T; *Y ^= (*T <<  2);
    	*T = ((*Y >>  8) ^ *X) & 0x00ff00ff; *X ^= *T; *Y ^= (*T <<  8);
    	*Y = ((*Y << 1) | (*Y >> 31)) & 0xffffffff;
    	*T = (*X ^ *Y) & 0xaaaaaaaa; *Y ^= *T; *X ^= *T;
	*X = ((*X << 1) | (*X >> 31)) & 0xffffffff;
}

inline void DES_ROUND(des_state *cs, u32 *X, u32 *Y, u32 *T)
{
	u32 *rk_ptr = cs->rk;
	*T = *rk_ptr ^ *X; rk_ptr++;
    	*Y ^= Sbox8[ (*T      ) & 0x3f ] ^
         Sbox6[ (*T >>  8) & 0x3f ] ^
         Sbox4[ (*T >> 16) & 0x3f ] ^
         Sbox2[ (*T >> 24) & 0x3f ];

    	*T = *rk_ptr++ ^ ((*X << 28) | (*X >> 4));
    	*Y ^= Sbox7[ (*T      ) & 0x3f ] ^
         Sbox5[ (*T >>  8) & 0x3f ] ^
         Sbox3[ (*T >> 16) & 0x3f ] ^
         Sbox1[ (*T >> 24) & 0x3f ];
}

inline void DES_FP(u32 *X, u32 *Y, u32 *T)
{
	*X = ((*X << 31) | (*X >> 1)) & 0xffffffff;
    	*T = (*X ^ *Y) & 0xaaaaaaaa; *X ^= *T; *Y ^= *T;
    	*Y = ((*Y << 31) | (*Y >> 1)) & 0xffffffff;
    	*T = ((*Y >>  8) ^ *X) & 0x00ff00ff; *X ^= *T; *Y ^= (*T <<  8);
    	*T = ((*Y >>  2) ^ *X) & 0x33333333; *X ^= *T; *Y ^= (*T <<  2);
    	*T = ((*X >> 16) ^ *Y) & 0x0000ffff; *Y ^= *T; *X ^= (*T << 16);
    	*T = ((*X >>  4) ^ *Y) & 0x0f0f0f0f; *Y ^= *T; *X ^= (*T <<  4);
}

